
#include "Scene.h"

//////////////////////////////////////////////////////////////////////

Scene::Scene()
{
	m_vPrimitives.clear();

	m_cAmbient.fRed = 0.0f;
	m_cAmbient.fGreen = 0.0f;
	m_cAmbient.fBlue = 0.0f;
}

//////////////////////////////////////////////////////////////////////

int Scene::addPrimitive(Primitive *p)
{
	m_vPrimitives.push_back(p);

	return 0;
}

//////////////////////////////////////////////////////////////////////

int Scene::getNumPrimitives()
{
	return m_vPrimitives.size();
}

//////////////////////////////////////////////////////////////////////

Primitive* Scene::getPrimitive(int i)
{
	return m_vPrimitives.at(i);
}

//////////////////////////////////////////////////////////////////////

int Scene::setAmbientLight(Color &c)
{
	m_cAmbient = c;

	return 0;
}

//////////////////////////////////////////////////////////////////////

int Scene::getAmbientLight(Color &c)
{
	c = m_cAmbient;

	return 0;
}

