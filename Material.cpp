
#include "Material.h"

//////////////////////////////////////////////////////////////////////

int Material::setPigment(Color &c)
{
	m_cPigment = c;

	return 0;
}

//////////////////////////////////////////////////////////////////////

int Material::getPigment(Color &c)
{
	c = m_cPigment;

	return 0;
}

//////////////////////////////////////////////////////////////////////

const Color Material::getPigment()
{
	return m_cPigment;
}

//////////////////////////////////////////////////////////////////////

int Material::setAmbient(float fAmbient)
{
	if (fAmbient < 0.0f || fAmbient > 1.0f)
		return 1;

	m_fAmbient = fAmbient;

	return 0;
}

//////////////////////////////////////////////////////////////////////

float Material::getAmbient()
{
	return m_fAmbient;
}

//////////////////////////////////////////////////////////////////////

int Material::setDiffuse(float fDiffuse)
{
	m_fDiffuse = fDiffuse;
	return 0;
}

//////////////////////////////////////////////////////////////////////

float Material::getDiffuse()
{
	return m_fDiffuse;
}

//////////////////////////////////////////////////////////////////////

int Material::setSpecular(float fSpecular)
{
	m_fSpecular = fSpecular;
	return 0;
}

//////////////////////////////////////////////////////////////////////

float Material::getSpecular()
{
	return m_fSpecular;
}
